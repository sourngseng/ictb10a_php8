<?php 
   $x = 10;
   echo ++$x; //Outputs: 11
   echo "<br>";
   echo $x; //Outputs: 11
   echo "<br>";
   
   $x = 10;
   echo $x++; //Outputs: 10
   echo "<br>";
   echo $x; //Outputs: 11
   echo "<br>";

   $x = 10;
   echo --$x; //Outputs: 9
   echo "<br>";
   echo $x; //Outputs: 9
   echo "<br>";

   $x = 10;
   echo $x--; //Outputs: 10
   echo "<br>";
   echo $x; //Outputs: 9
   
?>